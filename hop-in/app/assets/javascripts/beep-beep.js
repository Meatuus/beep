$(document).ready(function() {

$('.login-btn').on('click', function() {

  $('.login-btn').fadeOut('fast');
  $('.signup-btn').fadeOut('fast');
  $('.login').fadeIn('slow');
});

$('.signup-btn').on('click', function() {

  $('.login-btn').fadeOut('fast');
  $('.signup-btn').fadeOut('fast');
  $('.signup-container').fadeIn('slow');
});










});
